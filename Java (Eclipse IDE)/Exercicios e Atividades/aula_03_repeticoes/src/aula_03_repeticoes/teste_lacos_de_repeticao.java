package aula_03_repeticoes;

public class teste_lacos_de_repeticao {

	public static void main(String[] args) {
		int contador = 1;
		
		while(contador <= 10) {
			System.out.println("While = "+contador);
			contador++;
		}
		
		for(int i = 1; i <= 10; i++) {
			System.out.println("For = "+i);
		}
		
		
		
	}
}
